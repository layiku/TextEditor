/*
 * platform.h — 平台抽象层接口
 * display.c 和 input.c 通过此接口调用平台相关功能；
 * 具体实现在 platform_win.c / platform_nix.c / platform_dos.c 中。
 *
 * 自动平台检测：
 *   _WIN32         → platform_win.c
 *   __MSDOS__      → platform_dos.c
 *   其余（Linux 等）→ platform_nix.c
 */
#pragma once

#include "types.h"

/* ================================================================
 * 生命周期
 * ================================================================ */
/* 初始化控制台（切换到原始模式、保存终端状态、启用鼠标等）
 * 必须在所有其他 plat_* 调用之前执行 */
void plat_init(void);

/* 还原终端状态，退出前调用 */
void plat_exit(void);

/* ================================================================
 * 屏幕尺寸
 * ================================================================ */
/* 获取当前终端行列数，写入 *rows, *cols */
void plat_get_size(int *rows, int *cols);

/* ================================================================
 * 屏幕输出（由 display_flush 调用，一次性输出变化区域）
 * ================================================================ */
/* 在屏幕 (y, x) 位置写出 count 个连续单元格（字符 + 属性）
 * 调用前应确保光标在正确位置 */
void plat_write_cells(int y, int x, const Cell *cells, int count);

/* 移动终端光标到 (y, x)（0-based） */
void plat_set_cursor(int y, int x);

/* 显示或隐藏硬件光标（show=1 显示，show=0 隐藏） */
void plat_show_cursor(int show);

/* 清除整个屏幕，用 attr 对应的背景色填充 */
void plat_clear_screen(uint8_t attr);

/* ================================================================
 * 输入（非阻塞轮询）
 * ================================================================ */
/* 等待事件，最多阻塞 timeout_ms 毫秒（0 = 不阻塞，-1 = 一直阻塞）
 * 有事件时填入 *ev 并返回 1；无事件返回 0；错误返回 -1 */
int plat_poll_event(InputEvent *ev, int timeout_ms);

