/*
 * utf8.h — UTF-8 编解码与显示宽度工具
 *
 * 设计原则：
 *   - 纯 C 标准库，零外部依赖
 *   - 编辑器内部始终以 UTF-8 字节流存储文档
 *   - "字节偏移"(byte_off) = 行缓冲区中的字节索引
 *   - "显示列"(col)        = 屏幕上的列号（宽字符占 2 列，ASCII 占 1 列）
 *   - 编辑器 cursor_col 使用字节偏移；viewport 负责转换为显示列
 */
#pragma once

#include <stdint.h>

/* ================================================================
 * UTF-8 序列长度与解码
 * ================================================================ */

/* 由首字节推断该 UTF-8 序列的字节数（1-4）。
 * 无效首字节返回 1（以跳过错误字节的方式降级处理）。 */
int utf8_seq_len(unsigned char first_byte);

/* 从 s 解码一个 Unicode 码点，写入 *out_cp，返回消耗的字节数（1-4）。
 * s 不能为 NULL；遇到非法序列时 *out_cp = 0xFFFD（替换字符），返回 1。 */
int utf8_decode(const char *s, uint32_t *out_cp);

/* 将 Unicode 码点 cp 编码为 UTF-8，写入 out（需至少 4 字节空间），
 * 返回写入的字节数（1-4）。cp=0 时写入 '\0' 并返回 1。 */
int utf8_encode(uint32_t cp, char *out);

/* ================================================================
 * 显示宽度
 * ================================================================ */

/* 返回码点 cp 在等宽字体终端中占用的显示列数：
 *   2 = 全角（CJK 统一表意文字、全角符号等）
 *   1 = 其余可打印字符、控制字符（包括 \t，调用方自行展开）
 * 本函数不处理组合字符（Combining），按 1 列计。 */
int utf8_cp_width(uint32_t cp);

/* ================================================================
 * 行内坐标换算
 * 参数说明：
 *   line      — 行文本（UTF-8 字节流，无 \0 也可以，由 byte_len 限定）
 *   byte_len  — 行有效字节数（不含 \0）
 *   byte_off  — 字节偏移（0 = 行首）
 *   col       — 显示列（0 = 最左列）
 * ================================================================ */

/* 字节偏移 → 显示列（从 0 计，宽字符占 2 列）。
 * byte_off 超出范围时夹紧到 [0, byte_len]。 */
int utf8_byte_to_col(const char *line, int byte_len, int byte_off);

/* 显示列 → 字节偏移。若 col 落在宽字符中间，返回该字符的起始字节。
 * col 超出范围时返回 byte_len（行末）。 */
int utf8_col_to_byte(const char *line, int byte_len, int col);

/* 整行的总显示宽度（所有码点宽度之和）。 */
int utf8_line_display_width(const char *line, int byte_len);

/* ================================================================
 * 光标移动（字节偏移级别，跳过多字节序列）
 * ================================================================ */

/* 从 byte_off 向右移动一个码点，返回新字节偏移（最大 byte_len）。 */
int utf8_next_char(const char *line, int byte_len, int byte_off);

/* 从 byte_off 向左移动一个码点，返回新字节偏移（最小 0）。 */
int utf8_prev_char(const char *line, int byte_off);
